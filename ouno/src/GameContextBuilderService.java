    public void divide_cards(ArrayList<Robots> players, Client client, Bank bank) {

        //دسته کارت client پر میشود
        for (int i = 0; i < 7; i++) {
            int random = (int) (Math.random() * (108));
            Cards sample;
            sample = bank.getAllCards().get(random);
            client.getHandCards().add(sample);
            bank.getAllCards().remove(random);
            for (int j = 0 ; i < 7 ; i++){
                System.out.println(j+" = " + "( "+client.getHandCards().get(j).getColor() + client.getHandCards().get(j).getType()+ " )");
            }
            System.out.println(client.getHandCards());
        }
        //دسته کارت هر robot پر میشود
        for (Robots item : players) {
            for (int i = 0; i < 7; i++) {
                int random = (int) (Math.random() * (108));
                String color;
                String type;
                Cards card = bank.getAllCards().get(random);
                color = card.getColor();
                type = card.getType();
                Cards sample = new Cards(color, type);
                item.getHandCards().add(sample);
                bank.getAllCards().remove(random);
            }

        }
    }

    public void creat_bank(Bank bank) {

        for (Cards item : bank.getAllCards()) {
            bank.getRemainCards(item).add(item);
        }

    }

